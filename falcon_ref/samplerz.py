RCDT = RCDT_FLOOR


# C contains the coefficients of a polynomial that approximates exp(-x)
# More precisely, the value:
# (2 ** -63) * sum(C[12 - i] * (x ** i) for i in range(i))
# Should be very close to exp(-x).
# This polynomial is lifted from FACCT: https://doi.org/10.1109/TC.2019.2940949
C = [
    0x00000004741183A3,
    0x00000036548CFC06,
    0x0000024FDCBF140A,
    0x0000171D939DE045,
    0x0000D00CF58F6F84,
    0x000680681CF796E3,
    0x002D82D8305B0FEA,
    0x011111110E066FD0,
    0x0555555555070F00,
    0x155555555581FF00,
    0x400000000002B400,
    0x7FFFFFFFFFFF4800,
    0x8000000000000000]


def basesampler(randombytes=urandom, mode=None):
    """
    Sample z0 in {0, 1, ..., 18} with a distribution close to a
    half-Gaussian of parameter MAX_SIGMA, centered on 0 (mode="floor")
    or 1/2 (mode="round"). Takes as (optional) input the randomness
    source (default: urandom). When ``mode`` is None, falls back to the
    module-level ``SAMPLERZ_MODE``.
    """
    if mode is None:
        mode = SAMPLERZ_MODE
    rcdt = RCDT_FLOOR if mode == "floor" else RCDT_ROUND
    u = int.from_bytes(randombytes(RCDT_PREC >> 3), "little")

    z0 = 0
    for elt in rcdt:
        z0 += int(u < elt)
    return z0


def approxexp(x, ccs):
    """
    Compute an approximation of 2^63 * ccs * exp(-x).

    Input:
    - a floating-point number x
    - a scaling factor ccs
    Both inputs x and ccs MUST be positive.

    Output:
    - an integral approximation of 2^63 * ccs * exp(-x).
    """
    # y, z are always positive
    y = C[0]
    # Since z is positive, int is equivalent to floor
    z = int(x * (1 << 63))
    for elt in C[1:]:
        y = elt - ((z * y) >> 63)
    z = int(ccs * (1 << 63)) << 1
    y = (z * y) >> 63
    return y


def berexp(x, ccs, randombytes=urandom):
    """
    Return a single bit, equal to 1 with probability ~ ccs * exp(-x).
    Both inputs x and ccs MUST be positive.
    Also takes as (optional) input the randomness source (default: urandom).
    """
    s = int(x * ILN2)
    r = x - s * LN2
    s = min(s, 63)
    z = (approxexp(r, ccs) - 1) >> s
    for i in range(56, -8, -8):
        p = int.from_bytes(randombytes(1), "little")
        w = p - ((z >> i) & 0xFF)
        if w:
            break
    return (w < 0)


def samplerz(mu, sigma, sigmin, randombytes=urandom, mode=None):
    """
    Given floating-point values mu, sigma (and sigmin),
    output an integer z according to the discrete
    Gaussian distribution D_{Z, mu, sigma}.

    Input:
    - the center mu
    - the standard deviation sigma
    - a scaling factor sigmin
    - optional: the randomness source randombytes (default: urandom)
      randombytes(k) should output k pseudorandom bytes
    - optional: ``mode`` ∈ {"floor", "round"} selecting the s/r split
      convention. ``floor`` = Falcon spec; ``round`` = LTYZ NewSamplerZ
      (Algorithm 4 of paper 2024-1709). When None, uses the module-
      level ``SAMPLERZ_MODE``.
    The inputs MUST verify 1 < sigmin < sigma < MAX_SIGMA.

    Output:
    - a sample z from the distribution D_{Z, mu, sigma}.
    """
    if mode is None:
        mode = SAMPLERZ_MODE

    if mode == "floor":
        s = int(floor(mu))
    else:
        # Python 3 round() = round-half-to-even (banker's), matching ⌊·⌉
        # in LTYZ. Returns int, no precision loss for typical Falcon mu.
        s = round(mu)
    r = mu - s
    dss = 1 / (2 * sigma * sigma)
    ccs = sigmin / sigma

    while(1):
        # Sample z0 from a half-Gaussian (mode-specific RCDT).
        z0 = basesampler(randombytes=randombytes, mode=mode)
        # Bimodal extension: z = b + (2b−1)·z0 for floor (Falcon spec),
        # or z = (2b−1)·z0 for round (LTYZ Algorithm 4).
        b = int.from_bytes(randombytes(1), "little")
        b &= 1
        if mode == "floor":
            z = b + (2 * b - 1) * z0
            # Falcon classic rejection: x = (z − r)²·dss − z0²·INV_2SIGMA2.
            # The proxy distribution D_{Z⁺, σmax, 0} has rho ∝ exp(-z0²/(2σmax²)),
            # which is exactly cancelled by the +z0²/(2σmax²) accept factor,
            # leaving the desired Gaussian factor exp(-(z-r)²/(2σ²)).
            sigma_correction = z0 ** 2
        else:
            z = (2 * b - 1) * z0
            # LTYZ Algorithm 4 line 5: x = (z − r)²·dss − (z0² − z0)·INV_2SIGMA2.
            # The proxy is D_{Z⁺, σmax, 1/2} with halved D(0), whose rho ∝
            # exp(-(z0 − 1/2)²/(2σmax²)). The (z0² − z0)/(2σmax²) accept term
            # cancels exactly: -(z0 − 1/2)² + (z0² − z0) = -1/4 (constant in z0),
            # yielding the desired Gaussian factor exp(-(z-r)²/(2σ²)).
            sigma_correction = z0 ** 2 - z0
        x = ((z - r) ** 2) * dss
        x -= sigma_correction * INV_2SIGMA2
        if berexp(x, ccs, randombytes=randombytes):
            return z + s
