"""
Reference Gaussian sampler over the integers (samplerz) for Falcon, in
float64. (Modified) copy of the upstream Thomas Prest reference at
https://github.com/tprest/falcon.py, kept here as a runtime dependency
for `falcon_ref/test.py`. Production fxp code uses the deterministic
port at `fxp/samplerz_fxp.py` (bit-identical under shared
randomness modulo banker's-shift rounding).

Two split-and-sample modes are supported, switchable at module load time
or per call:

  ``floor`` (default, Falcon spec): ``s = floor(c)``, ``r = c − s ∈ [0, 1)``,
            bimodal extension ``z = b + (2b−1)·z₀`` for ``z₀ ∈ Z⁺``.
            Numerically unstable when c is near an integer (LTYZ
            paper 2024-1709 attack surface).

  ``round`` (LTYZ Algorithm 4 NewSamplerZ): ``s = round(c)`` (banker's,
            round-half-to-even via Python's built-in ``round``),
            ``r = c − s ∈ [−1/2, 1/2)``, bimodal extension
            ``z = (2b−1)·z₀`` for ``z₀ ∈ Z⁺``. Numerically unstable at
            half-integers — but those don't occur in Falcon under the
            paper's keygen restriction ``‖(g, −f)‖² odd``, so this shifts
            the round-off-sensitive locus out of harm's way.

Both modes use the same rejection formula
``x = (z − r)²·dss − z₀²·INV_2SIGMA2`` and the same berexp+approxexp.
The only differences are the s/r split convention, the bimodal extension,
and the base RCDT (different center). See ``scripts/generate_rcdt.py``
for how the two RCDTs are derived (Howe-Prest-Ricosset-Rossi 2019-1411
§5.2 rounding procedure, applied to the half-Gaussian centered at 0
resp. 1/2).
"""

# Importing dependencies
from math import floor

# Use high-quality randomness
# The "secrets" library could also work (Python >= 3.6)
from os import urandom


# Upper bound on all the values of sigma
MAX_SIGMA = 1.8205
INV_2SIGMA2 = 1 / (2 * (MAX_SIGMA ** 2))

# Precision of RCDT
RCDT_PREC = 72

# ln(2) and 1 / ln(2), with ln the natural logarithm
LN2 = 0.69314718056
ILN2 = 1.44269504089


# Default samplerz mode for module-level callers (samplerz can also be
# called with an explicit mode= keyword to override). "floor" reproduces
# the Falcon spec verbatim; "round" implements LTYZ's NewSamplerZ.
SAMPLERZ_MODE = "floor"


# RCDT_FLOOR: half-Gaussian centered at μ = 0, no halving D(0). Matches
